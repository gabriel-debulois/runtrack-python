def calcul(p1, op1, p2):
    if op1 == "+":
        print(p1 + p2)
    if op1 == "-":
        print(p1 - p2)
    if op1 == "*":
        print(p1 * p2)
    if op1 == "/":
        print(p1 / p2)
    if op1 == "%":
        print(p1 % p2)


calcul(25, "+", 5)
calcul(25, "-", 5)
calcul(25, "*", 5)
calcul(25, "/", 5)
calcul(25, "%", 5)